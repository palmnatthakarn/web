class ReportProcessor {
  final BuildContext context;
  final TabController tabController;
  final Function(bool) onProcessingChanged;
  bool _isProcessing = false;

  ReportProcessor({
    required this.context,
    required this.tabController,
    required this.onProcessingChanged,
  });

  bool get isProcessing => _isProcessing;

  /// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ï‡∏≤‡∏°‡πÄ‡∏á‡∏∑‡πà‡∏≠‡∏ô‡πÑ‡∏Ç‡∏ó‡∏µ‡πà‡πÄ‡∏•‡∏∑‡∏≠‡∏Å
  Future<void> processReport() async {
    final reportBloc = context.read<ReportBloc>();
    final state = reportBloc.state;

    if (state is ReportLoaded) {
      // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
      if (state.startDate != null && state.endDate != null) {
        _isProcessing = true;
        onProcessingChanged(true);

        print('üöÄ ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô...');
        print('üìÖ ‡∏ä‡πà‡∏ß‡∏á‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà: ${state.startDate} - ${state.endDate}');
        
        try { 
          // ‡∏Å‡∏£‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ï‡∏≤‡∏°‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà‡∏Å‡πà‡∏≠‡∏ô‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•
          _filterReportsByDate(state.startDate!, state.endDate!);
          
          // ‡∏à‡∏≥‡∏•‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•
          await Future.delayed(const Duration(seconds: 2));

          // ‡∏£‡∏≠‡πÉ‡∏´‡πâ state ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏Å‡πà‡∏≠‡∏ô‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö
          await Future.delayed(const Duration(milliseconds: 500));
          final updatedState = reportBloc.state;
          
          if (updatedState is ReportLoaded && updatedState.filteredReports.isNotEmpty) {
            print('‚úÖ ‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à: ${updatedState.filteredReports.length} ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£');
            print('üìä ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏µ‡πà‡∏Å‡∏£‡∏≠‡∏á‡πÅ‡∏•‡πâ‡∏ß: ${updatedState.filteredReports.length} ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£');
            
            // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï step ‡πÑ‡∏õ‡∏¢‡∏±‡∏á‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏ñ‡∏±‡∏î‡πÑ‡∏õ
            reportBloc.add(SetStepEvent(1));
            
            // ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Å‡∏≤‡∏£‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•
            reportBloc.add(UpdateCardDataEvent(0, {
              'dateRange': '${DateFormat('dd/MM/yyyy').format(state.startDate!)} - ${DateFormat('dd/MM/yyyy').format(state.endDate!)}',
              'totalReports': updatedState.filteredReports.length,
              'processed': true,
            }));
            
            _isProcessing = false;
            onProcessingChanged(false);

            // ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÑ‡∏õ tab ‡∏ñ‡∏±‡∏î‡πÑ‡∏õ
            if (tabController.index < tabController.length - 1) {
              tabController.animateTo(tabController.index + 1);
            }
            // ‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à
            _showSuccessSnackBar();
          } else {
            _isProcessing = false;
            onProcessingChanged(false);
            _showNoDataSnackBar();
          }
        } catch (e) {
          _isProcessing = false;
          onProcessingChanged(false);
          print('‚ùå ‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•: $e');
          _showErrorSnackBar();
        }
      } else {
        // ‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà
        _showWarningSnackBar();
      }
    } else {
      _showErrorSnackBar();
    }
  }

  /// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏Å‡∏£‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ï‡∏≤‡∏°‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà
  void _filterReportsByDate(DateTime start, DateTime end) {
    final reportBloc = context.read<ReportBloc>();
    final state = reportBloc.state;

    if (state is ReportLoaded) {
      print('üîç Filtering reports by date...');
      print('üìä Total reports before filter: ${state.allReports.length}');
      print('üìÖ Date range: ${DateFormat('dd/MM/yyyy').format(start)} - ${DateFormat('dd/MM/yyyy').format(end)}');

      // ‡πÉ‡∏ä‡πâ allReports ‡πÄ‡∏õ‡πá‡∏ô‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏Å‡∏£‡∏≠‡∏á
      final allReports = state.allReports;

      final filtered = allReports.where((item) {
        // ‡πÄ‡∏õ‡∏£‡∏µ‡∏¢‡∏ö‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà ‡πÇ‡∏î‡∏¢‡πÑ‡∏°‡πà‡∏™‡∏ô‡πÉ‡∏à‡πÄ‡∏ß‡∏•‡∏≤
        final itemDate =
            DateTime(item.docDate.year, item.docDate.month, item.docDate.day);
        final startDate = DateTime(start.year, start.month, start.day);
        final endDate = DateTime(end.year, end.month, end.day);

        return (itemDate.isAtSameMomentAs(startDate) ||
                itemDate.isAfter(startDate)) &&
            (itemDate.isAtSameMomentAs(endDate) || itemDate.isBefore(endDate));
      }).toList();

      print('üìä Filtered reports count: ${filtered.length}');

      if (filtered.isEmpty) {
        print('‚ö†Ô∏è No reports found in selected date range');
      } else {
        print('‚úÖ Found ${filtered.length} reports in date range');
        // ‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ï‡∏±‡∏ß‡∏≠‡∏¢‡πà‡∏≤‡∏á‡∏Ç‡∏≠‡∏á‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡πÅ‡∏£‡∏Å
        final firstItem = filtered.first;
        print(
            'üìÑ First item date: ${DateFormat('dd/MM/yyyy').format(firstItem.docDate)}');
      }
      // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï filteredReports ‡πÉ‡∏ô state
      reportBloc.add(UpdateFilteredReportsEvent(filtered));
    } else {
      print('‚ùå State is not ReportLoaded: ${state.runtimeType}');
    }
  }

  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÄ‡∏™‡∏£‡∏¥‡∏°‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÄ‡∏£‡∏µ‡∏¢‡∏Å‡πÉ‡∏ä‡πâ‡∏Å‡∏≤‡∏£‡∏Å‡∏£‡∏≠‡∏á‡πÅ‡∏¢‡∏Å‡∏à‡∏≤‡∏Å‡∏Å‡∏≤‡∏£‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•
  void filterReportsOnly(DateTime start, DateTime end) {
    _filterReportsByDate(start, end);
  }

